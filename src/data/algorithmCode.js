export function activeCodeLine(experimentId, algorithm, progress) {
  const snippet = algorithmCode[experimentId]?.[algorithm]
  if (!snippet) return 0
  const position = Math.min(snippet.trace.length - 1, Math.floor(progress * snippet.trace.length))
  return snippet.trace[position]
}
